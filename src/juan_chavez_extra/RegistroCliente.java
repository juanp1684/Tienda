    private void registrar() {
        Connection conn = MisUtiles.getConection();
        try {
            String sql = "insert into cliente (nombrecliente, ncarnet, telefono)values(?,?,?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, getText(nombre));
            ps.setString(2, getText(carnet));
            ps.setString(3, getText(telefono));
            int res = ps.executeUpdate();
            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Registrado exitosamente " + getCredenciales());
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Error al registrar");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void limpiarCajas(){
        nombre.setText("");
        carnet.setText("");
        telefono.setText("");
    }

    private String getText(JTextField componente) {
        String res = componente.getText();
        if (res.equals("")) {
            return null;
        }
        return res;
    }

    private String getCredenciales() {
        Connection conn = MisUtiles.getConection();
        try {
            String sql = "SELECT codigocliente from cliente Where ncarnet = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            String password = getText(carnet);
            ps.setString(1, password);
            ResultSet rs = ps.executeQuery();
            String codigo = "";
            if (rs.next()) {
                codigo = Integer.toString(rs.getInt("codigocliente"));
            }
            return "\ncodigo de cliente: " + codigo
                    + "\n contrase√±a: " + password + " (carnet)";

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return "";
    }

